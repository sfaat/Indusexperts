                establishment of various types of businesses in our country. The various company types in India are
                highlighted in the section that follows.</p>
        </div>


        <div class="col-lg-12">
            <h5>Public Limited Company</h5>
        </div>

        <div class="col-lg-12">
            <p style="text-align: justify;">One whose stock is traded publicly is referred to as a public limited company. The number of shares
                that may be traded or sold in this type of corporation is unlimited. A public limited company&#39;s shares
                can be freely sold because they are listed on the Stock Exchange, making the shareholders the
                company&#39;s owners.</p>
            <p style="text-align: justify;">Such businesses must obtain a registration certificate from the ROC before starting their commercial
                operations. The public limited company must now comply with the requirements in order to register
                for this type of business.</p>


            <ul class="list-unstyled advantages-list">
                <li>The public limited company must be managed by a minimum of three directors.</li>
                <li>Indian descent is required for one of the three directors.</li>
                <li>There must be a minimum of seven shareholders for a publicly traded company.</li>
                <li>A capital fee of at least INR 5 Lakhs is also acceptable.</li>
                <li>It is necessary to have a registered business address in Indian territory.</li>

            </ul>
        </div>


        <div class="col-lg-12">
            <h5>Private Limited Company</h5>
        </div>

        <div class="col-lg-12">
            <p style="text-align: justify;">Businesses that want to function like privately held companies should use private limited companies.
                In a privately held company, responsibility is shared among the shareholders rather than assigned to a single employee. This enables the members to safeguard their assets against any losses sustained
                by the business.
            </p>
            <p style="text-align: justify;">The total number of shares that each member owns makes up the total capital of a private limited
                company. The corporate and personal assets of the members are regarded as separate, improving
                safety and assurance. Shares of such a company are not available to the general public for sale or
                disclosure. It is currently one of the most popular ways to register a business in the nation. The
                following prerequisites for forming a privately held company were established by the 2013
                Companies Act:</p>
            <ul class="list-unstyled advantages-list">
                <li>There could be as few as two directors and as many as fifteen in a private limited company.</li>
                <li>The directors must all have Indian ancestry at least.</li>
                <li>There can be as few as two shareholders and as many as 200 in a private limited company.</li>
                <li>A capital fee of at least INR 1 lakh is also acceptable.</li>
                <li>must have a company that is registered in the country.</li>

            </ul>
        </div>
