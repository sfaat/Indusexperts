            </div>


        </div>

        <div class="row py-2 my-2">
            <div class="col-lg-12">
                <h5 class="py-1 my-1">Limited by Shares</h4>
                    <p style="font-size: 20px; text-align: justify">In such companies, a shareholder&#39;s liability is only as great as their shareholding capacity.</p>

            </div>


        </div>

        <div class="row py-2 my-2">
            <div class="col-lg-12">
                <h5 class="py-1 my-1">Limited by Guarantee</h5>
                <p style="font-size: 20px; text-align: justify">In this instance, the shareholders’ responsibility would be equal to the contribution they committed
                    to providing if the firm went bankrupt.</p>
            </div>
        </div>



        <div class="row py-2 my-2">
            <div class="col-lg-12">
                <h5 class="py-1 my-1">Unlimited</h5>
                <p style="font-size: 20px; text-align: justify">In this sort of business, the member’s responsibility is unlimited, meaning it is not limited in any way.
                    This means that the member must pay for the loss with their assets in the event of a financial
                    catastrophe.</p>

            </div>


            <div class="col-lg-12 py-2 my-2">
                <h5 class="py-1 my-1">Partnerships</h3>
                    <p style="font-size: 20px; text-align: justify">Another common method of company formation in India is partnerships. The Indian Partnership Act
                        of 1932 serves as the legal framework for these businesses. The partners in this business are in
                        charge of overseeing daily operations after the written agreement. The positions, responsibilities,
                        functions, and share count will now be listed in the partnership deed. If a partnership has a valid and
                        properly registered company partnership deed, it can operate without a licence. The partnership
                        firm must satisfy the following criteria in order to be eligible for this type of business:</p>

            </div>

            <div class="col-lg-12 py-2 my-2">
                <ul class="list-unstyled advantages-list">
                    <li>There could be as few as two partners in a partnership company and as many as ten.</li>
                    <li>They are required to have an Indian Territory office address.</li>
                    <li>The partnership contract must be signed by all parties.</li>
                </ul>

            </div>

            <div class="col-lg-12 py-2 my-2">
                <h5 class="py-1 my-1">Limited Liability Partnership</h3>
                    <p style="font-size: 20px; text-align: justify">A new type of company registration in India is the Limited Liability Partnership, or LLP. It also has a
                        legal identity that helps distinguish between business and personal assets and shields the
                        entrepreneur from unlimited liability.</p>

                    <p style="font-size: 20px; text-align: justify">An LLP offers more protection than a sole proprietorship because the amount of share capital
                        determines the responsibility of each partner. Now, the LLP must comply with the following
                        conditions in order to obtain registration for such a company type:</p>

                    <ul class="list-unstyled advantages-list">
                        <li>A share capital requirement of at least one lakh is necessary.</li>
                        <li>Indian descent is required for at least one of the partners.</li>
                        <li>To start this kind of business, you need at least two partners. The upper bound is uncapped.</li>
                        <li>If the remaining partners are corporations, there should be at least one individual partner.</li>
                        <li>The purchase of share capital is not subject to any restrictions.</li>
                    </ul>

            </div>

            <div class="col-lg-12 py-2 my-2">
                <h5 class="py-1 my-1">One Person Company</h3>
                    <p style="font-size: 20px; text-align: justify">The one-person firm model is most advantageous to small businesses. This business form, which is a
                        part of the 2013 Companies Act, enables business owners who intend to operate their enterprises
                        solely on their own. The legal position of One Person Company gives it total control over its
                        operations, assets, and liabilities. The following conditions must all be met without exception by the
                        OPC in order for this type of company to be eligible for registration: -</p>

