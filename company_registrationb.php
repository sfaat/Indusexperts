                        conditions in order to obtain registration for such a company type:</p>





                    <ul class="list-unstyled advantages-list">
                        <li>It is necessary to have a minimum share capital of one lakh rupees.</li>
                        <li>The company&#39;s owner should be an Indian.</li>
                        <li> The candidate must be chosen by the promoters in order for the company to be incorporated.</li>
                        <li>
                            Lending institutions for money are not considered one-person businesses.</li>
                        <li>The firm should be converted to a privately owned company if the paid-up capital exceeds INR 50
                            lakhs and the annual turnover exceeds two crores.</li>
                    </ul>

            </div>

            <div class="col-lg-12 py-2 my-2">
                <h5 class="py-1 my-1">Section 8 Company</h3>
                    <p style="font-size: 20px; text-align: justify">A non-profit organisation is another name for a section 8 corporation. Human welfare is the main
                        concern of section 8 businesses. Such businesses are prohibited from using the profits they receive
                        for personal gain; instead, they must use them to further their objectives as an organisation. The
                        Section-8 corporation must fulfil the following criteria in order to be eligible for this type of business
                        model:</p>
                    <ul class="list-unstyled advantages-list">
                        <li>There must be at least two stockholders.</li>
                        <li>Directors for Section 8 companies must number at least two.</li>
                        <li> The directors should all be of Indian descent.</li>
                        <li>

                            There are no limitations on the amount of money required.</li>
                        <li>The company must be a registered entity with a physical presence in the country.</li>
                    </ul>

            </div>

            <div class="col-lg-12 py-2 my-2">
                <h5 class="py-1 my-1">Sole Proprietorship</h3>
                    <p style="font-size: 20px; text-align: justify">Due to its ability to enable people to start their own businesses, this is conceivably India&#39;s most
                        popular business model. However, in such a business model, the owner and the company are seen
                        as a single entity, making them entirely accountable for gain or loss.</p>
                    <p style="font-size: 20px; text-align: justify">Additionally, since the owner&#39;s name appears on the registration, the owner&#39;s name will appear on
                        accounting reports and tax filings as well, creating an endless amount of responsibility. A sole
                        proprietorship does not have a separate business registration procedure as a result.</p>
